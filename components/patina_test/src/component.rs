use patina::component::{Storage, component};

/// A filter to include or exclude test cases whose name contains the pattern.
///
/// # Example
///
/// ```rust
/// use patina_test::component::{TestRunner, Filter};
///
/// let runner = TestRunner::default()
///     .with_filter(Filter::include("x64"))
///     .with_filter(Filter::exclude("aarch64"));
/// ```
#[derive(Debug, Clone)]
pub enum Filter {
    /// Only run tests whose name contains the pattern.
    Include(&'static str),
    /// Do not run tests whose name contains the pattern.
    Exclude(&'static str),
}

impl Filter {
    /// Creates an include filter. Tests whose name contains `pattern` will be included.
    pub fn include(pattern: &'static str) -> Self {
        Self::Include(pattern)
    }

    /// Creates an exclude filter. Tests whose name contains `pattern` will be excluded.
    pub fn exclude(pattern: &'static str) -> Self {
        Self::Exclude(pattern)
    }
}

/// A component that runs all test cases marked with the `#[patina_test]` attribute when loaded by the DXE core.
#[derive(Default, Clone)]
pub struct TestRunner {
    filters: Vec<Filter>,
    debug_mode: bool,
    fail_callback: Option<fn(&'static str, &'static str)>,
}

#[component]
impl TestRunner {
    /// Adds a filter to control which tests are executed.
    ///
    /// The filter pattern is matched against the full test name, which includes the module path.
    /// For example, if a test is defined in `my_crate::tests`, the name would be
    /// `my_crate::tests::test_case`.
    ///
    /// - [`Filter::Include`]: Only run tests whose name contains the pattern. When multiple include
    ///   filters are specified, a test runs if it matches **any** of them.
    /// - [`Filter::Exclude`]: Prevents tests whose name contains the pattern from running. Exclude
    ///   filters take priority over include filters.
    ///
    /// This filter is case-sensitive. It can be called multiple times to add multiple filters.
    pub fn with_filter(mut self, filter: Filter) -> Self {
        self.filters.push(filter);
        self
    }

    /// Any log messages generated by the test case will be logged if this is set to true.
    ///
    /// Defaults to false.
    pub fn debug_mode(mut self, debug_mode: bool) -> Self {
        self.debug_mode = debug_mode;
        self
    }

    /// Attach a callback function that will be called on test failure.
    ///
    /// fn(test_name: &'static str, fail_msg: &'static str)
    pub fn with_callback(mut self, callback: fn(&'static str, &'static str)) -> Self {
        self.fail_callback = Some(callback);
        self
    }

    /// The entry point for the test runner component.
    #[coverage(off)]
    fn entry_point(self, storage: &mut Storage) -> patina::error::Result<()> {
        let test_list: &'static [__private_api::TestCase] = __private_api::test_cases();
        self.register_tests(test_list, storage)
    }

    /// Registers the tests to be executed by the test runner.
    fn register_tests(
        &self,
        test_list: &'static [__private_api::TestCase],
        storage: &mut Storage,
    ) -> patina::error::Result<()> {
        let recorder = match storage.get_service::<Recorder>() {
            Some(recorder) => recorder,
            None => {
                let recorder = Recorder::default();
                recorder.initialize(storage)?;
                storage.add_service(recorder);
                storage.get_service::<Recorder>().expect("Recorder service should be registered.")
            }
        };

        let records = test_list
            .iter()
            .filter(|&test_case| test_case.should_run(self.filters.as_slice()))
            .map(|test_case| TestRecord::new(self.debug_mode, test_case, self.fail_callback));

        for record in records {
            // Only schedule a run if we have not already scheduled for this test.
            if !recorder.test_registered(record.name()) {
                record.schedule_run(storage)?;
            }

            recorder.update_record(record);
        }

        Ok(())
    }
}

